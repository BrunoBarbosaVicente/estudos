# Operadores aritimeticos

5 + 2 # Adição = 7
5 - 2 # Subtração = 3
5 * 2 # Multiplicação = 10
5 / 2 # Divisão = 2.5
5 ** 2 # Potencia = 25
5 // 2 # Divisão inteira = 2
5 % 2 # Resto da divisão = 1

# Ordem de procedência

# 1- () parenteses
# 2- ** potencias
# 3- * multiplicação, / Divisão, // Divisão inteira, % Resto da divisão
# 4- + Soma, - Subtração