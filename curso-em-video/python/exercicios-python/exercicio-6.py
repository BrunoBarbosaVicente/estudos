# Faça um programa que leia um número inteiro e mostre na tela o seu sucessor e seu antecessor.

n = int(input('Digite um número:'))
nA = n - 1
pN = n + 1
print(f'A ordem dos numeros são, {nA}, {n} e {pN}')
print(f'A ordem do numeros são, {n-1}, {n} e {n+1}') # com essa formatação evitaremos criar variavel desnecessarias