# Toda linguagem de programação, trabalha basicamente com quatro tipos primitivos, o python ate tem mais tipos, veremos em breve, a priori os quatro que são mais fundamentais.

int() # Recebe numeros inteiros ex: 7, -4, 0, 9875
float() # Recebe numeros flutuantes ex: 4.5, 0.076, -15.223, 7.0
bool() # Recebe dois valores True ou False, que siguinifica Verdadeiro ou Falso
str() # Recebe valores do tipo mensagem que todo valor deve estar entre aspas ex: 'Olá', '7.5', '', as variaveis de tipo string que so contem aspas como valor, significa uma string vazia.